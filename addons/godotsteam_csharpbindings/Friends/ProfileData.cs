namespace GodotSteam;

/// <summary>

/// The profile data class

/// </summary>

public class ProfileData
{
    /// <summary>
    /// Gets or sets the value of the avatar animated
    /// </summary>
    public bool AvatarAnimated { get; set; }
    /// <summary>
    /// Gets or sets the value of the avatar frame
    /// </summary>
    public bool AvatarFrame { get; set; }
    /// <summary>
    /// Gets or sets the value of the profile modifier
    /// </summary>
    public bool ProfileModifier { get; set; }
    /// <summary>
    /// Gets or sets the value of the profile background
    /// </summary>
    public bool ProfileBackground { get; set; }
    /// <summary>
    /// Gets or sets the value of the profile mini background
    /// </summary>
    public bool ProfileMiniBackground { get; set; }
}