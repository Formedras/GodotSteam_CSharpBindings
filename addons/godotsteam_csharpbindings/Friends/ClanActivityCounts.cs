namespace GodotSteam;

/// <summary>

/// The clan activity counts class

/// </summary>

public class ClanActivityCounts
{
    /// <summary>
    /// Gets or sets the value of the clan
    /// </summary>
    public ulong Clan { get; set; }
    /// <summary>
    /// Gets or sets the value of the online
    /// </summary>
    public int Online { get; set; }
